import Foundation
import UIKit
import SwiftUI

public extension UIColor {
    static var rowndPurple: UIColor {
        return UIColor(red: 0x5B / 255.0, green: 0x13 / 255.0, blue: 0xDF / 255.0, alpha: 1.0)
    }
}

public extension Color {
    static var rowndPurple: Color {
        return Color(red: 0x5B / 255.0, green: 0x13 / 255.0, blue: 0xDF / 255.0)
    }
}
